int		ft_atoi(char *str)
{
	int c;
	int atoi;

	c = 0;
	atoi = 0;
	while (*str != '\0' && (*str == ' ' || *str == '\t' || *str == '\v'
				|| *str == '\f' || *str == '\r' || *str == '\n'))
		str++;
	while (*str != '\0' && (*str == '+' || *str == '-'))
	{
		if (*str == '-')
			c++;
		str++;
	}
	while (*str != '\0' && (*str >= '0' && *str <= '9'))
	{
		atoi = atoi * 10 + (*str - '0');
		str++;
	}
	if (c % 2 == 1)
		atoi *= -1;
	return (atoi);
}
